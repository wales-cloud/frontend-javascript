// Define the base interface
interface Teacher {
  readonly firstName: string;
  readonly lastName: string;
  fullTimeEmployee: boolean;
  yearsOfExperience?: number;
  location: string;
  [key: string]: any;
}

// Extend Teacher to create Directors
interface Directors extends Teacher {
  numberOfReports: number;
}

// Test case
const director1: Directors = {
  firstName: 'John',
  lastName: 'Doe',
  location: 'London',
  fullTimeEmployee: true,
  numberOfReports: 17,
};

console.log(director1);

// Interface for the formatting function
interface printTeacherFunction {
  (firstName: string, lastName: string): string;
}

// Function implementation
const printTeacher: printTeacherFunction = (firstName, lastName) => {
  return `${firstName.charAt(0)}. ${lastName}`;
};

// Example usage
console.log(printTeacher("John", "Doe")); // Output: J. Doe

// Interface to describe the constructor signature
interface StudentConstructor {
  new (firstName: string, lastName: string): StudentInterface;
}

// Interface to describe class structure
interface StudentInterface {
  workOnHomework(): string;
  displayName(): string;
}

// Implement the class using the interface
class StudentClass implements StudentInterface {
  constructor(private firstName: string, private lastName: string) {}

  workOnHomework(): string {
    return "Currently working";
  }

  displayName(): string {
    return this.firstName;
  }
}

// Example usage
const student = new StudentClass("Jane", "Doe");
console.log(student.displayName());     // Output: Jane
console.log(student.workOnHomework()); // Output: Currently working
